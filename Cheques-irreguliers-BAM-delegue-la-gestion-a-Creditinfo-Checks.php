                                                    <p>

                                                    Afin de contrer la progression des impayés sur chèque et restaurer la crédibilité vis-à-vis de ce moyen
                                                    de paiement, Bank Al-Maghrib vient de signer une convention de délégation avec Checkinfo, filiale du
                                                    groupe international d’origine islandaise Creditinfo. Cette convention a été signée à l’issue d’un appel
                                                    à manifestation d’intérêt dont Checkinfo est sorti vainqueur et porte sur la gestion du Service de
                                                    Centralisation des Chèques Irréguliers (SCCI).

                                                    </p>
                                                    <p>
                                                        Le SCCI centralise les données communiquées par Bank Al-Maghrib :
                                                        <ul>
                                                            <li>
                                                                - Les relevés d’identité bancaire des comptes de chèques qui sont clôturés, frappés
                                                                   d’indisponibilité ou qui sont ouverts au nom d'une personne interdite d'émettre des chèques ;
                                                            </li>
                                                            <li>
                                                                - Les éléments d’identification des chèques ayant fait l’objet d’une opposition pour perte, vol,
                                                                  utilisation frauduleuse ou falsification.
                                                            </li>
                                                        </ul>
                                                    </p>

                                                    <p>
                                                    Checkinfo fournira l’accès au SCCI aux sociétés, professionnels libéraux et auto-entrepreneurs inscris au
                                                    service. L’objectif de Checkinfo sera de leur permettre de vérifier la régularité d’un chèque avant son
                                                    acceptation et de réduire ainsi le risque de non-encaissement. Pour ce faire, il suffira de saisir des données
                                                    au niveau de la plateforme (le RIB et le numéro de chèque notamment). La vérification pourra
                                                    également se faire grâce à la lecture de la ligne codée en bas du chèque.
                                                    </p>

                                                    <p>
                                                    Pour avoir accès à Checkinfo, toute personne éligible pourra souscrire au service en ligne, sur le site
                                                    <a href="www.checkinfo.ma.">www.checkinfo.ma.</a> Ce service sera bien sûr accessible sur l’ensemble du territoire et plusieurs systèmes
                                                    de tarification seront mis en place, en fonction du profil utilisateur et du volume de consultation.
                                                    </p>

                                                    <p>
                                                    En parallèle, Checkinfo lancera également un service d’opposition sur chèque, destiné à toute
                                                    personne ayant perdu son chèque / chéquier et souhaitant procéder à une opposition préalablement
                                                    à la déclaration d’opposition qui doit être faite auprès de l’établissement bancaire de référence.
                                                    </p>
                                                    
                                                    <p>
                                                    D’autres prestations seront lancées par la suite, comme le scoring et le blocage de provision.
                                                    </p>

                                                    <p>
                                                    Checkinfo vient compléter un dispositif mis en place par Bank Al-Maghrib et qui a pour but de prévenir
                                                    et lutter contre l’émission de chèques impayés. Il s’agit notamment de la Centrale des Incidents de
                                                    Paiement, que tout établissement bancaire est tenu de consulter avant de délivrer un chéquier à l’un de
                                                    ses clients.
                                                    </p>

