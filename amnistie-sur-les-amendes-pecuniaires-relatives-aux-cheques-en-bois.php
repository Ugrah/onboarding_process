                                                        <p>
                                                        Un amendement au Projet de Loi de Finances 2020, présenté par la Trésorerie Générale du Royaume a
                                                        été adopté par la Commission des Finances à la Première Chambre dans la nuit du 12 au 13 novembre
                                                        2020. Cet amendement prévoit une amnistie sur les amendes pécuniaires relatives aux incidents de
                                                        paiement qui n’ont pas encore été régularisés, et ce, quel que soit leur rang.
                                                        </p>
                                                        <p>
                                                            Actuellement, les amendes sont réparties comme suit :
                                                            <ul>
                                                                <li> - 5% du montant du/des chèque(s) impayé(s) faisant l’objet de la première injonction ;</li>
                                                                <li> - 10% du montant du/des chèque(s) impayé(s) faisant l’objet de la deuxième injonction ;</li>
                                                                <li> - 20% du montant du/des chèque(s) impayé(s) faisant l’objet de la troisième injonction et
                                                                    suivantes.</li>
                                                            </ul>
                                                        </p>
                                                        
                                                        <p>
                                                        L’amendement adopté la nuit dernière prévoit une contribution libératoire estimée à 1,5% du montant
                                                        des chèques. Cette contribution est plafonnée à 10.000 Dhs pour les personnes physiques et 50.000 Dhs
                                                        pour les personnes morales, quels que soient le montant et le nombre des incidents de paiement.
                                                        </p>
                                                        <p>
                                                        Cet amendement concerne les incidents de paiement déclarés jusqu’au 31.12.2019 et non encore
                                                        régularisés, à condition d’en effectuer le paiement en un seul versement, durant l’année 2020 à la
                                                        Trésorerie Générale du Royaume.
                                                        </p>

                                                        <p>
                                                        Cet amendement, conçu en concertation avec Bank Al-Maghrib, est une nouvelle mesure visant à
                                                        assainir la situation du chèque puisque, rappelons-le, ce ne sont pas moins de 3,2 millions d’incidents de
                                                        paiements qui n’ont toujours pas été régularisés, pour un montant de plus 95 Milliards de Dirhams et qui
                                                        concernent plus de 670.000 personnes actuellement interdites de chéquier dont 87,5% de personnes
                                                        physiques. Cette décision, qui vise à ramener ces personnes à la « normalité » afin qu’elles puissent
                                                        reprendre des activités saines, est l’une des nombreuses mesures initiées par Bank Al-Maghrib visant à
                                                        rétablir la crédibilité du chèque comme moyen de paiement. L’une des prochaines mesures qui devrait
                                                        être mise en place est la mise à disposition des commerçants de l’information relative à la régularité des
                                                        chèques via la société Checkinfo qui assurera la gestion du Service de Centralisation des Chèques
                                                        Irréguliers.
